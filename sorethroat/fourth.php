<strong>Management for Painful blisters on lips/mouth </strong><br>

<ul>
<li> <strong>Aphthous ulcer/s likely</strong>, give as needed  for pain : lidocaine 4% applied to ulcers 6 hourly or rinse
 with aspirin 600mg in water hourly.</li>

 <li>Refere medical officer if :<br>
   -Not healed within 3 weeks <br>
   -Ulcer > 1cm</li>




<li>Advice the patient with sore mouth/throat to avoid spicy, hot, dry acidic food and eat soft mosit food </li>
<li>Advise to keep mouth and teeth clean by brushing and rinsing regularly </li>

