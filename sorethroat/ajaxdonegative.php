<h4 style="color:blue">Management for malaria case negative</h4>
<h5>Consider other causes of fever</h5>
<ul>
  <li>If patient has any other symptoms, manage sympton on symptom page </li>
  <li> If fever > 1 week and abdominal pain, <strong> typhoid fever </strong> likely. Send blood/stool for culture and give ciprofloxin
       500mg orally 12 hourly for 10 days. Medical officer to review with result after 3 days . Advice patient to return immediately if symptoms worsen.</li>
  <li>If fever >= 2 weeks, exclude TB</li>
  <li>If status unknown, test for HIV</li>
  <li>If cause is uncertain, or fever > 3 weeks, refer medical officer</li>
</ul>

