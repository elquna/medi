<strong>Management for Painful blisters on lips/mouth </strong><br>

<ul>
<li> <strong>Herpes simplex likely</strong>, give as needed for pain :<br> -lidocaine 4% on blisters 8 hourly.<br>
-Paracetamol 1g orally 6 hourly up to 5 days .</li>

<li> Give aciclovir 200mg orally 5 times a day for 7 days. start as soon as possible after onset of symptoms.</li>
<li>if status is not known, test for HIV</li>



<li>Advice the patient with sore mouth/throat to avoid spicy, hot, dry acidic food and eat soft mosit food </li>
<li>Advise to keep mouth and teeth clean by brushing and rinsing regularly </li>

