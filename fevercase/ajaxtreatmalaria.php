<strong> Case wherethe patient has not recieved treatment for malaria in previous 4 weeks</strong>
<h5>Give  1st line treatment</h4>
  <li>If Pregnant and not in 1st trimester or not pregnant, give athesunate/lumefantrine 80/480mg orally with food and milk immediately
  , then after 8 hours, then after 12 hours for 2 days (total 6 doses). </li>

  <li> If pregnant in 1st trimester, give instead for 7 days: quinine sulphate 10mgKg orally 12 hours and clindamycin 10mg/Kg orally 12 hourly</li>
  <li> If unable to tolerate oral medication or symptoms of severity develop as in emergency, refer medical officer</li>
  <li>Encourage patient to use long-lasting insecticide treated net or insecticide spray, cover up exposed skin with long sleeve/shirt
    and remove/fill collections of water where mosquitoes breed.</li>



