<h4 style="color:blue">Management for Positive  Parasite slide microscopy</h4>
<h5>Give 2nd line treatment</h5>
<ul>
  <li>If pregnant and not in 1st trimester or not in pregnant, give artesunate/amodiaquine 200/540mg orally daily for 3 days </li>
  <li> If pregnant in 1st trimester, give instead for 7 days : artesunate 4mg/Kg orally daily and clindamycin 10mg/kg orally 12 hourly </li>
  <li>If patient on zidovudine or efavirenz, give instead artesunate/mefloquine 200/440mg orally daily for 3 days </li>

  <li> If unable to tolerate oral medication or symptoms of severity develop as in emergency, refer medical officer</li>
  <li>Encourage patient to use long-lasting insecticide treated net or insecticide spray, cover up exposed skin with long sleeve/shirt
    and remove/fill collections of water where mosquitoes breed.</li>


</ul>

